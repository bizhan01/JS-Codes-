function convertCurrency(amount, exchangeRate) {
    return amount * exchangeRate;
}

const amountInUSD = 100;
const exchangeRate = 0.85; // EUR to USD
console.log(`Equivalent amount in EUR: ${convertCurrency(amountInUSD, exchangeRate)}`);



